                        <h1>The Viral 'Tiramisu Cake' Dance Challenge</h1>
                        <p><span id=blog-author>Regina Kim</span> | 30 July 2024</p>
                        <hr>
                        <p>In recent weeks the so-called "Tiramisu Cake" dance challenge has taken over Korean social media on platforms like TikTok, YouTube and Instagram. People from all walks of life—from Korean elementary school students to office workers to K-pop stars—have been posting videos of themselves dancing to an electronic indie pop song titled "Tiramisu Cake."</p>
                        <p>The "Tiramisu Cake" dance typically starts with the person shaking their hips while holding one arm up in the air. Then they will raise both arms before lowering them back down, all the while continuing to shake their hips in time to the chorus. After that, some users will turn around and repeat all or some of these moves, while others might begin freestyling.</p>
                        <p>The video went viral, with Korean netizens remarking that they found both the song and the choreo to be quite addictive. Many of them soon began posting videos of themselves dancing along to the song on YouTube, TikTok and Instagram, and hence the "Tiramisu Cake" dance challenge was born.</p>
                        <p>While most of the videos feature one or more persons dancing, some users have posted their own variations on the challenge, such as lifting weights or waving their pets' paws in time to the song. Zepeto, Asia's largest metaverse platform that is owned by Korean tech conglomerate Naver, also started its own "Tiramisu Cake" dance challenge by inviting its users to share videos of their avatars dancing to the song.</p>
                        <p>It's a bit crazy to think that a song that was released nine years ago is currently going viral, but that's the beauty and power of social media. For the band We Are The Night, seeing tons of people in Korea and other countries dancing to their song has been a welcome surprise. They are really glad that so many people are listening to their music thanks to this dance challenge.</p>
